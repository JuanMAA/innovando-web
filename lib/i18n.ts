export type Lang = keyof typeof translations
export type T = typeof translations.es
export type A11y = { title: string; darkMode: string; lightMode: string; fontSize: string; highContrast: string; reset: string; menu: string; close: string }

const VALID_LANGS = Object.keys(translations) as Lang[]

export function getLang(raw?: string | null): Lang {
  if (raw && VALID_LANGS.includes(raw as Lang)) return raw as Lang
  // Try short code (e.g. "fr-FR" → "fr")
  if (raw) {
    const short = raw.split('-')[0].toLowerCase()
    if (VALID_LANGS.includes(short as Lang)) return short as Lang
  }
  return 'es'
}

export function t(lang: Lang): T {
  return translations[lang] as unknown as T
}
